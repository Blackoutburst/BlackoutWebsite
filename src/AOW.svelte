    <h1 class="features-title">Demo</h1>
    <iframe class="features-vid" src="https://www.youtube.com/embed/Fy0pyhpQIkM" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
    <h1 class="features-title">Features</h1>
    <p>
        For this section I will mainly repeat everything said on the <a href="https://github.com/Blackoutburst/AoW-plugin" target="_blank" rel="noopener noreferrer">GitHub</a><br>
        This game works by spawning unit that need to go destroy the opponent base while protecting your base<br>
        Every units walk on a straight line and follow each other, some of them have the ability to shoot from behind<br>
        In total this game contains 15 differents units (3 per age)<br>
        <br>
        All NPCs and health bars/holos in general are made with my own custom API<br>
        <a href="https://github.com/Blackoutburst/NPC-API" target="_blank" rel="noopener noreferrer">NPC API</a><br>
        <a href="https://github.com/Blackoutburst/Holo-API" target="_blank" rel="noopener noreferrer">Holo API</a><br>
    </p>
    <img src="img/allunits.png" alt="allunits" class="features-img">
    <p class="note">(Here you can see all units in order)</p>

    <p>
        Buying unit work with two differents way<br>
        You can either spawn new units using shop npcs that display the unis looks and important stats<br>
    </p>
    <img src="img/shopnpc.png" alt="shopnpcs" class="features-img">
    <p class="note">(The 4th npc is used to evolve to the next age therefore it doesn't appear during the last age)</p>

    <p>
        And the second way of buying them is by using items<br>
        Every player in their hand has items corresponding to the items held by the units<br>
        <br>
        An important note with buying units is the experience bar display a cooldown after buying units<br>
        As it's not possible to spam units you need to wait a certain amount of time between them
    </p>
    <img src="img/items.png" alt="items" class="features-img">
    <p class="note">(All item follow the NPC order to avoid confusion and yes we will talk about the first item later)</p>

    <p>
        This game works with 5 different ages, the order being<br>
        - Prehistoric<br>
        - Medieval<br>
        - Renaissance<br>
        - Modern<br>
        - Futuristic<br>
        <br>
        Each age contains their own units and base look and special skills<br>
        Right below you can see some example of bases
    </p>
    <img src="img/base1.png" alt="base1" class="features-img-custom">
    <img src="img/base2.png" alt="base2" class="features-img-custom">
    <img src="img/base3.png" alt="base3" class="features-img-custom">
    <p class="note">(Can you guess which base belong to which age ?)</p>
    <p>
        And finally the last but probably best feature: the special skills<br>
        Each age does have their own special skill set under a 60 seconds cooldown after use<br>
        <br>
        The skill is used with an item displaying a blaze powder if ready to use<br>
        Otherwise gray dye with the number being the cooldown before the next use<br>
        <br>
        I tried my best to respect the original games skill so here are some example below<br>
    </p>
    <img src="img/skill1.png" alt="skill1" class="features-img-custom">
